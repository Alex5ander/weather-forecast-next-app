async function getData(q) {
  try {
    const url = `http://api.weatherapi.com/v1/current.json?key=${
      process.env.API_KEY
    }&q=${encodeURI(q)}&aqi=no&lang=pt`;

    const request = await fetch(url, { next: { revalidate: 100 } });

    const json = await request.json();

    return json;
  } catch (error) {
    return null;
  }
}

